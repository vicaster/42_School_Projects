#include "libft.h"

int		ft_lenint(int n)
{
	int				i;
	unsigned int	j;

	i = 0;
	j = (n < 0 ? -n : n);
	if (n < 0)
		i += 1;
	while (j /= 10)
		i++;
	return (i);
}
