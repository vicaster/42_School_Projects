#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
		{
			return ((char*)s + i);
		}
		i--;
	}
	if (s[i] == '\0' && c == '\0')
		return ((char*)s + 1);
	return (0);
}
